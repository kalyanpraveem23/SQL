#Find the total quantity sold for each product.

SELECT product_id,
       SUM(quantity) AS SoldProduct
FROM Orders
GROUP BY product_id;


#Find the customer who has ordered the highest total number of items.

select c.Customers_id,c.name,
	   SUM(o.quantity) AS total_item_ordered
from Orders o
join c on o. customer_id = c .customer_id
Group by c.customer_id ,c .name
Order by  total_item_ordered desc
LIMIT 1 ;

#Show the total revenue (price × quantity) generated by each product.

SELECT p.product_id,
       p.product_name,
       SUM(p.price * o.quantity) AS total_revenue
FROM Orders o
JOIN Products p
    ON o.product_id = p.product_id
GROUP BY p.product_id, p.product_name;

#Q8: List all customers and their order IDs, including those who haven't placed any orders

SELECT c.customer_id,
       c.name,
       o.order_id
FROM Customers c
LEFT JOIN Orders o
    ON c.customer_id = o.customer_id;
    
# For each customer, display their order ID and a running total of quantity ordered (ordered by date).   

SELECT customer_id,
       order_id,
       order_date,
       quantity,
       SUM(quantity) OVER (
           PARTITION BY customer_id
           ORDER BY order_date
       ) AS running_total
FROM Orders;




